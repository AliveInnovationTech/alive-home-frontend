// Helper functions for working with property data
export const getPropertyById = (id: string) => {
  return propertiesData.find((property) => property.id === parseInt(id));
};

export const getPropertyFeatures = (propertyId: string) => {
  const property = getPropertyById(propertyId);
  return property ? property.features : [];
};

export const getPropertiesByPriceRange = (
  minPrice: number,
  maxPrice: number
) => {
  return propertiesData.filter(
    (property) => property.price >= minPrice && property.price <= maxPrice
  );
};

export const getPropertiesByBedrooms = (bedrooms: number) => {
  return propertiesData.filter((property) => property.beds === bedrooms);
};
